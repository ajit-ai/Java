package basic.Addition;

public class AdditionDemo {

    
    public static void main(String[] args) {
        int x = 1;
        System.out.println(x);

        x++;
        System.out.println(x);
        x++;
        System.out.println(x);
        x--;
        System.out.println(x);
        --x;
        System.out.println(x);
    }
}
